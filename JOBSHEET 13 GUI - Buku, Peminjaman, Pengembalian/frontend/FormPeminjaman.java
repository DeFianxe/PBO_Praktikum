    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            int idPeminjaman = Integer.parseInt(txtIdPeminjaman.getText());
            int idAnggota = Integer.parseInt(txtIdAnggota.getText());
            int idBuku = Integer.parseInt(txtIdBuku.getText());

            Peminjaman peminjaman = new Peminjaman();
            peminjaman.setIdPeminjaman(idPeminjaman);
            peminjaman.setAnggota(new Anggota().getById(idAnggota));
            peminjaman.setBuku(new Buku().getById(idBuku));
            peminjaman.setTanggalPinjam(txtTanggalPinjam.getText());
            peminjaman.setTanggalKembali(txtTanggalKembali.getText());
            peminjaman.setStatus(false);

            peminjaman.save();
            tampilkanDataPeminjaman();
            kosongkanForm();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Input ID Peminjaman, Anggota, and Buku must be numeric values.");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int idPeminjaman = Integer.parseInt(txtIdPeminjaman.getText());

        if (idPeminjaman != 0) {
            new Peminjaman().getById(idPeminjaman).delete();
            tampilkanDataPeminjaman();
            kosongkanForm();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
        } else {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        String keyword = txtCari.getText();
        String[] kolom = {"ID", "Anggota", "Buku", "Tanggal Pinjam", "Tanggal Kembali", "Status"};
        ArrayList<Peminjaman> list = new Peminjaman().search(keyword);
        Object rowData[] = new Object[6];

        tblPeminjaman.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                kolom
        ));

        for (Peminjaman pinjam : list) {
            rowData[0] = pinjam.getIdPeminjaman();
            rowData[1] = pinjam.getAnggota().getNama();
            rowData[2] = pinjam.getBuku().getJudul();
            rowData[3] = pinjam.getTanggalPinjam();
            rowData[4] = pinjam.getTanggalKembali();
            rowData[5] = pinjam.getStatus() ? "Dikembalikan" : "Belum Dikembalikan";

            ((javax.swing.table.DefaultTableModel) tblPeminjaman.getModel()).addRow(rowData);
        }
    }//GEN-LAST:event_btnCariActionPerformed

    private void tblPeminjamanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPeminjamanMouseClicked
        // TODO add your handling code here:
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblPeminjaman.getModel();
        int row = tblPeminjaman.getSelectedRow();

        txtIdPeminjaman.setText(model.getValueAt(row, 0).toString());
        Anggota anggota = new Anggota().getByName(model.getValueAt(row, 1).toString());
        Buku buku = new Buku().getByTitle(model.getValueAt(row, 2).toString());
        txtIdAnggota.setText(String.valueOf(anggota.getIdanggota()));
        txtNamaAnggota.setText(anggota.getNama());
        txtIdBuku.setText(String.valueOf(buku.getIdbuku()));
        txtJudulBuku.setText(buku.getJudul());
        txtTanggalPinjam.setText(model.getValueAt(row, 3).toString());
        txtTanggalKembali.setText(model.getValueAt(row, 4).toString());
    }//GEN-LAST:event_tblPeminjamanMouseClicked

