    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
         int idPeminjaman = Integer.parseInt(txtIdPeminjaman.getText());
        Peminjaman peminjaman = new Peminjaman().getById(idPeminjaman);

        if (peminjaman != null && !peminjaman.getStatus()) {
            txtNamaAnggota.setText(peminjaman.getAnggota().getNama());
            txtJudulBuku.setText(peminjaman.getBuku().getJudul());
            txtTanggalPinjam.setText(peminjaman.getTanggalPinjam());
            txtTanggalKembali.setText(peminjaman.getTanggalKembali());
            txtTanggalPengembalian.setText(java.time.LocalDate.now().toString());
        } else if (peminjaman != null && peminjaman.getStatus()) {
            JOptionPane.showMessageDialog(this, "Buku ini sudah dikembalikan.");
        } else {
            JOptionPane.showMessageDialog(this, "Peminjaman tidak ditemukan.");
        }
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        int idPeminjaman = Integer.parseInt(txtIdPeminjaman.getText());
        Peminjaman peminjaman = new Peminjaman().getById(idPeminjaman);

        if (peminjaman != null && !peminjaman.getStatus()) {
            Pengembalian pengembalian = new Pengembalian(peminjaman, txtTanggalPengembalian.getText());
            pengembalian.save();
            tampilkanDataPengembalian();
            kosongkanForm();
            JOptionPane.showMessageDialog(this, "Pengembalian berhasil disimpan.");
        } else {
            JOptionPane.showMessageDialog(this, "Pengembalian gagal, cek ID Peminjaman.");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

