    private void tampilkanDataPengembalian() {
        String[] kolom = {"ID Pengembalian", "ID Peminjaman", "Nama Anggota", "Judul Buku", "Tanggal Pinjam", "Tanggal Kembali", "Tanggal Pengembalian"};
        ArrayList<Pengembalian> list = new Pengembalian().getAll();
        Object rowData[] = new Object[7];

        tblPengembalian.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                kolom
        ));

        for (Pengembalian pengembalian : list) {
            rowData[0] = pengembalian.getIdPengembalian();
            rowData[1] = pengembalian.getPeminjaman().getIdPeminjaman();
            rowData[2] = pengembalian.getPeminjaman().getAnggota().getNama();
            rowData[3] = pengembalian.getPeminjaman().getBuku().getJudul();
            rowData[4] = pengembalian.getPeminjaman().getTanggalPinjam();
            rowData[5] = pengembalian.getPeminjaman().getTanggalKembali();
            rowData[6] = pengembalian.getTanggalPengembalian();

            ((javax.swing.table.DefaultTableModel) tblPengembalian.getModel()).addRow(rowData);
        }
    }

